    /**
     * Remove the tutor from the tutoring database
     *
     * @param tutorNumber
     */
    public void removeTutor(String tutorNumber) {

        jLabel24.setVisible(true);
        int position = tutorNumbers.indexOf(tutorNumber);//get the position of the tutor in the array lists
        tutors.remove(position);//remove the tutor's name from the tutor name array list
        tutorGrades.remove(position);//remove the tutor's grade from the tutor grades array list
        tutorSubjects.remove(position);//remove the tutor's subject from the tutor subjects array list
        bookedStatus.remove(position);//remove the tutor's booked status from the booked status array list
        tutorNumbers.remove(position);//remove the tutor's student number from the array list

        int checkIfTutoring;

        checkIfTutoring = pairs.indexOf(tutorNumber);//check if the tutor is booked

        if (checkIfTutoring >= 0) {//if the tutor is booked
            int pos = pairs.indexOf(tutorNumber);
            int studentPos = pairs.indexOf(tutorNumber) - 1;//find out which student
            String studentNumber = pairs.get(studentPos);
            int spos = studentNumber.indexOf(studentNumber);
            learningStatus.set(spos, "false");//update their learning status so that they are no longer learning
            pairs.remove(pos);//remove the tutor from pairs
            pairs.remove(studentPos);//remove the student from pairs
            writeStudent();//update the student file 
            writePairs();//update the pairs file
        }

        writeTutor();//update the tutor file
    }

