    /**
     * Sort the students by grade (youngest to oldest)
     *
     * @param student
     * @param studentNum
     * @param studentGrade
     * @param studentSubject
     * @return
     */
    public static ArrayList studentGrade(ArrayList<String> student, ArrayList<String> studentNum, ArrayList<String> studentGrade, ArrayList<String> studentSubject) {

        int count = student.size();//let count store the number of items in the array list
        String temp;//String to store the greater grade when comparing two spots in an array list
        String numTemp;
        String gradeTemp;
        String subjectTemp;
        int compare;//int to store the outcome of comparing two names
        boolean sorted = false;//boolean to store whether the array list is sorted or not

        while (sorted == false) {//while the array list is not sorted in ascending order
            sorted = true;//assume the list is sorted

            for (int d = 0; d < (count - 1); d++) {//repeat to compare all the items in the list
                int first = Integer.parseInt(studentGrade.get(d));
                int second = Integer.parseInt(studentGrade.get(d + 1));

                if (first > second) {//if the first student is older than the second student

                    sorted = false;//the array list is not sorted because it's supposed to be in ascending order           

                    temp = student.get(d);//store the older student's name
                    numTemp = studentNum.get(d);//store the older student's student number
                    gradeTemp = studentGrade.get(d);//store the older student's grade
                    subjectTemp = studentSubject.get(d);//store the older student's subject

                    //Switch the older student and younger student so that the younger student comes first
                    student.set(d, student.get(d + 1));
                    student.set(d + 1, temp);

                    studentNum.set(d, studentNum.get(d + 1));
                    studentNum.set(d + 1, numTemp);

                    studentGrade.set(d, studentGrade.get(d + 1));
                    studentGrade.set(d + 1, gradeTemp);

                    studentSubject.set(d, studentSubject.get(d + 1));
                    studentSubject.set(d + 1, subjectTemp);

                }
            }
        }

        return student;//return the sorted array list to main
    }


