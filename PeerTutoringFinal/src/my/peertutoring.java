    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Book a Tutor

        String name = jTextField10.getText();//get the student's name
        String studentNumber = jTextField11.getText();//get the student's student number
        int position = studentNumbers.indexOf(studentNumber);
        String learning = learningStatus.get(position);

        //Student Info
        String subject = studentSubjects.get(position);//get the subject
        String gradeString = studentGrades.get(position);//get the grade

        if (pairs.contains(studentNumber)) {
            jLabel40.setText("You have already booked a tutor");

        } else if (!pairs.contains(studentNumber)) {

            int grade = Integer.parseInt(gradeString);//turn the grade into an integer

            //Tutor Info
            String tutorSubject = "";//variable to store the tutor's subject
            String tGrade = "";//variable to store the tutor's grade
            int tutorGrade;//variable to store the tutor's grade in an integer
            String booked = "";//variable to store the tutor's booked status
            String matchedTutor = "";//variable to store the tutor's name
            String tutorNumber = "";//variable to store the tutor's student number

            int c = 0;

            boolean match = false;//at first, there is no match for the student
            int number = tutors.size() - 1;

            //search through arrays for the first matching tutor and display it on the screen
            while ((match == false) && (c <= number)) {//while a matching tutor has not been found       
                tutorSubject = tutorSubjects.get(c);
                tGrade = tutorGrades.get(c);
                tutorGrade = Integer.parseInt(tGrade);
                booked = bookedStatus.get(c);
                tutorNumber = tutorNumbers.get(c);

                if ((subject.equalsIgnoreCase(tutorSubject)) && (tutorGrade >= grade) && ("false".equals(booked))) {
                    match = true;
                    matchedTutor = tutors.get(c);

                } else if (match == false) {
                    c++;
                }

            }

            if (match == false) {
                jLabel36.setText("There are currently no tutors available for your tutoring.");

            } else if (match == true) {
                jLabel36.setText("Your " + subject + " tutor is: " + matchedTutor);
                booked = "true";
                Tutor newTutor = new Tutor(matchedTutor, tGrade, tutorSubject, booked, tutorNumber);//create a new tutor object
                bookedStatus.set(c, "true");//book the tutor
                newTutor.book(tutorNumber, studentNumber);
                //UPDATE THE STUDENT
                learningStatus.set(position, "true");//the student is now learning        
                writeStudent();

            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

