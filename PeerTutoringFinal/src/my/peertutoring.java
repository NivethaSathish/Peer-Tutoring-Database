    /**
     * Reads the tutor information in the tutors file to the array lists
     */
    private void readTutor() {
        if (tutorsFile.length() != 0) {//if a user is already stored in the users file, read in the old users into the array
            try {
                Scanner input = new Scanner(tutorsFile).useDelimiter("\\s*;\\s*");

                while (input.hasNext()) {//while there are still more users in the file that have not been read
                    String inName = input.next();
                    tutors.add(inName);//add the next first name to the firstNames array list

                    String inGrade = input.next();
                    tutorGrades.add(inGrade);//add the next username to the usernames array list

                    String inSubject = input.next();
                    tutorSubjects.add(inSubject);//add the next email address to the email addresses array list

                    String inBookStatus = input.next();
                    bookedStatus.add(inBookStatus);//add the next password to the passwords array list

                    String tutorNumber = input.next();
                    tutorNumbers.add(tutorNumber);//add the next tutor number to the tutor numbers array list
                }

            } catch (FileNotFoundException ex) {
                System.out.printf("ERROR %s\n", ex);
            }
        } else {
            System.out.printf("Nothing in file");
        }
    }

