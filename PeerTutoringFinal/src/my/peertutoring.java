    /**
     * Sort the tutors into alphabetical order
     *
     * @param tutor
     * @param tutorNum
     * @param tutorGrade
     * @param tutorSubject
     * @return
     */
    public static ArrayList tutorAlphabetical(ArrayList<String> tutor, ArrayList<String> tutorNum, ArrayList<String> tutorGrade, ArrayList<String> tutorSubject) {

        int count = tutor.size();//let count store the number of items in the array list
        String temp;//String to store the greater name when comparing two spots in an array list
        String numTemp;
        String gradeTemp;
        String subjectTemp;
        int compare;//int to store the outcome of comparing two names
        boolean sorted = false;//boolean to store whether the array list is sorted or not

        while (sorted == false) {//while the array list is not sorted in ascending order
            sorted = true;//assume the list is sorted
            for (int d = 0; d < (count - 1); d++) {//repeat to compare all the items in the list
                compare = tutor.get(d).compareTo(tutor.get(d + 1));//compare a name with the name right after it

                if (compare > 0) {//if the first name is greater (comes later in the alphabet) than the second name
                    sorted = false;//the array list is not sorted because it's supposed to be in ascending order           
                    temp = tutor.get(d);//let temp store the greater name
                    numTemp = tutorNum.get(d);//store the greater name's student number
                    gradeTemp = tutorGrade.get(d);//store the greater name's grade
                    subjectTemp = tutorSubject.get(d);//store the greater name's subject

                    //Switch the greater name and lesser name so that the one that comes first alphabetically comes at the beginning
                    tutor.set(d, tutor.get(d + 1));
                    tutor.set(d + 1, temp);

                    tutorNum.set(d, tutorNum.get(d + 1));
                    tutorNum.set(d + 1, numTemp);

                    tutorGrade.set(d, tutorGrade.get(d + 1));
                    tutorGrade.set(d + 1, gradeTemp);

                    tutorSubject.set(d, tutorSubject.get(d + 1));
                    tutorSubject.set(d + 1, subjectTemp);

                }
            }
        }

        return tutor;//return the sorted array list to main
    }

    /**
     * Sort the tutors into order by grade (youngest to oldest)
     *
     * @param tutor
     * @param tutorNum
     * @param tutorGrade
     * @param tutorSubject
     * @return
     */
    public static ArrayList tutorGrade(ArrayList<String> tutor, ArrayList<String> tutorNum, ArrayList<String> tutorGrade, ArrayList<String> tutorSubject) {

        int count = tutor.size();//let count store the number of items in the array list
        String temp;//String to store the greater name when comparing two spots in an array list
        String numTemp;
        String gradeTemp;
        String subjectTemp;
        int compare;//int to store the outcome of comparing two names
        boolean sorted = false;//boolean to store whether the array list is sorted or not

        while (sorted == false) {//while the array list is not sorted in ascending order
            sorted = true;//assume the list is sorted
            for (int d = 0; d < (count - 1); d++) {//repeat to compare all the items in the list

                int first = Integer.parseInt(tutorGrade.get(d));
                int second = Integer.parseInt(tutorGrade.get(d + 1));

                if (first > second) {//if the first tutor is older than the second tutor

                    sorted = false;//the array list is not sorted because it's supposed to be in ascending order           

                    temp = tutor.get(d);//store the older tutor's name
                    numTemp = tutorNum.get(d);//store the older tutor's student number
                    gradeTemp = tutorGrade.get(d);//store the older tutor's grade
                    subjectTemp = tutorSubject.get(d);//store the older tutor's subject

                    //Switch the younger tutor and older tutor so that the younger tutor comes first
                    tutor.set(d, tutor.get(d + 1));
                    tutor.set(d + 1, temp);

                    tutorNum.set(d, tutorNum.get(d + 1));
                    tutorNum.set(d + 1, numTemp);

                    tutorGrade.set(d, tutorGrade.get(d + 1));
                    tutorGrade.set(d + 1, gradeTemp);

                    tutorSubject.set(d, tutorSubject.get(d + 1));
                    tutorSubject.set(d + 1, subjectTemp);

                }
            }
        }

        return tutor;//return the sorted array list to main
    }

